func (twofish Twofish) EncryptECB(payload []byte) []byte {
	var result []byte = nil
	var l = len(payload)
	var c, pad byte = 0, 0
	var i = 0
	var j, k int
	var pt = make([]byte, twofishBlocksize)
	for i < l {
		if i+twofishBlocksize < l {
			pad = 0
		} else {
			pad = byte(i + twofishBlocksize - l)
		}
		k = 0
		for j = i; j < i+twofishBlocksize; j++ {
			if j < l {
				c = payload[j]
			} else {
				c = pad
			}
			pt[k] = c
			k++
		}
		i += twofishBlocksize
		twofish.EncryptBase(pt)
		result = append(result, pt[:]...)
	}
	if pad == 0 {
		var vpad = make([]byte, twofishBlocksize)
		for j = 0; j < twofishBlocksize; j++ {
			vpad[j] = byte(twofishBlocksize)
		}
		twofish.EncryptBase(vpad)
		result = append(result, vpad[:]...)
	}
	return result
}

func (twofish Twofish) DecryptECB(payload []byte) []byte {
	var result []byte = nil
	var l = len(payload)
	var c, p byte
	var i = 0
	var j, k, z int
	var pt = make([]byte, twofishBlocksize)
	for i < l {
		k = 0
		for j = i; j < i+twofishBlocksize; j++ {
			if j < l {
				c = payload[j]
			} else {
				c = 0
			}
			pt[k] = c
			k++
		}
		i += twofishBlocksize
		twofish.DecryptBase(pt)
		result = append(result, pt[:]...)
	}
	j = len(result)
	if j > 0 {
		p = result[j-1]
		if (int(p) <= twofishBlocksize) && (int(p) < j) {
			z = int(p)
			c = 0
			for z >= 0 {
				if result[j-z-1] == p {
					c++
				}
				z--
			}
			if c == p {
				result = result[:j-int(p)]
			}
		}
	}
	return result
}

func (twofish Twofish) EncryptCBC(payload, vinit []byte) []byte {
	var result []byte = nil
	var l = len(payload)
	var c, pad byte
	var i = 0
	var j, k int
	var pt, vxor = make([]byte, twofishBlocksize), make([]byte, twofishBlocksize)
	if len(vinit) > 0 {
		k = 0
		for j = 0; j < twofishBlocksize; j++ {
			if k >= len(vinit) {
				k = 0
			}
			vxor[j] = vinit[k]
			k++
		}
	}
	for i < l {
		if i+twofishBlocksize < l {
			pad = 0
		} else {
			pad = byte(i + twofishBlocksize - l)
		}
		k = 0
		for j = i; j < i+twofishBlocksize; j++ {
			if j < l {
				c = payload[j]
			} else {
				c = pad
			}
			pt[k] = c
			k++
		}
		i += twofishBlocksize
		for j = 0; j < twofishBlocksize; j++ {
			pt[j] ^= vxor[j]
		}
		twofish.EncryptBase(pt)
		copy(vxor, pt)
		result = append(result, pt[:]...)
	}
	if pad == 0 {
		var vpad = make([]byte, twofishBlocksize)
		for j = 0; j < twofishBlocksize; j++ {
			vpad[j] = byte(twofishBlocksize) ^ vxor[j]
		}
		twofish.EncryptBase(vpad)
		result = append(result, vpad[:]...)
	}
	return result
}

